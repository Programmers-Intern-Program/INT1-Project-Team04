-- mcp_server / mcp_tool 테이블 + monitoring-mcp + search_house_price 시드.
-- input_schema 는 mcp-server/scripts/export_tool_schema.py search_house_price 출력을 그대로 박음.
-- 도구 명세 변경 시 동일 스크립트 재실행 후 후속 마이그레이션(V3+)으로 input_schema 갱신.

CREATE TABLE IF NOT EXISTS mcp_server (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    description TEXT,
    endpoint TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS mcp_tool (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    server_id BIGINT NOT NULL REFERENCES mcp_server(id),
    domain_id BIGINT NOT NULL REFERENCES domain(id),
    name VARCHAR(100) NOT NULL,
    description TEXT,
    input_schema JSONB,
    UNIQUE (server_id, name)
);

-- application.yml 의 spring.ai.mcp.client.sse.connections.python-mcp.url 이
-- 실제 연결 시 우선. 아래 endpoint 는 docker-compose 내부 호스트 fallback.
