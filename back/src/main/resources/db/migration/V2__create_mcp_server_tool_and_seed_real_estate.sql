VALUES (1, 'monitoring-mcp', 'Python MCP server (FastMCP, SSE transport)', 'http://mcp-server:8090')
ON CONFLICT (id) DO UPDATE SET
    name = EXCLUDED.name,
    description = EXCLUDED.description,
    endpoint = EXCLUDED.endpoint;

